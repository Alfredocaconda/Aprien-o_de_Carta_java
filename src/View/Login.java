    private void login() {
        String usuario_nip =txtusuario.getText();
        String senha = txtsenha.getText();
        Mpolicia f= new Mpolicia();
        f.setSenha(senha);
        f.setUsuario(usuario_nip);
        Clogin l= new Clogin();
        if (l.consultar(f)==1) {
            new princpal().setVisible(true);
            this.dispose();
        } 
        if(l.consultar(f)==2){
             new automobilista().setVisible(true);
            this.dispose();
        }
    }
